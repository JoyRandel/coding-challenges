weather = "hot"
 
case weather
when "raining"
  puts "Get Your Umbrella!"
when "sunny"
  puts "Put on your sunglasses."
when "cloudy"
  puts "I love puffy clouds!"
when "hot"
  puts "Why don't you go swimming?"
when "cold"
  puts "Light Up another log in the fireplace!"
when 'freezing'
  puts "Let's go ice skating."
when "foggy"
  puts "I can't see a thing. It's too foggy."
else
  puts "Hmm, I'm not sure what that weather is like."
end